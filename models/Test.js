const mongoose = require('mongoose');

const testSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  questions: [
    {
      questionText: {
        type: String,
        required: true,
      },
      options: [
        {
          optionText: {
            type: String,
            required: true,
          },
          isCorrect: {
            type: Boolean,
            required: true,
          },
        },
      ],
    },
  ],
});

const Test = mongoose.model('Test', testSchema);

module.exports = Test;
